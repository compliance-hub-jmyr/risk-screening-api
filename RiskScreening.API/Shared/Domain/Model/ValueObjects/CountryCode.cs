using RiskScreening.API.Shared.Domain.Exceptions;

namespace RiskScreening.API.Shared.Domain.Model.ValueObjects;

/// <summary>ISO 3166-1 alpha-2 country code.</summary>
public record CountryCode : ValueObject, IComparable<CountryCode>
{
    public string Value { get; }

    public CountryCode(string value)
    {
        if (string.IsNullOrWhiteSpace(value))
            throw new InvalidValueException("CountryCode", value, "must not be blank",
                ErrorCodes.InvalidCountryCode, ErrorCodes.InvalidCountryCodeCode);

        var normalized = value.Trim().ToUpperInvariant();

        if (!ValidCodes.Contains(normalized))
            throw new InvalidValueException("CountryCode", value,
                "must be a valid ISO 3166-1 alpha-2 code",
                ErrorCodes.InvalidCountryCode, ErrorCodes.InvalidCountryCodeCode);

        Value = normalized;
    }

    public override string ToString()
    {
        return Value;
    }

    public static implicit operator string(CountryCode code)
    {
        return code.Value;
    }

    public int CompareTo(CountryCode? other)
    {
        return string.Compare(Value, other?.Value, StringComparison.Ordinal);
    }

